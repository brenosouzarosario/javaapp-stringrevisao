    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String texto = this.txtNumeros.getText();
    
    String [] vetorTexto = texto.split("/");
    
    int [] valor = new int [5];
    
    String numeroString ="";
    
    int ct=0;
    
    for (ct=0;ct<=4;ct++){
        numeroString = vetorTexto[ct];
        valor[ct]= Integer.valueOf(numeroString);
    }
    
    int menor = 100;
    int maior = 0;
    
    for(ct=0; ct<4; ct++){
        if(valor[ct]>=maior) maior=valor[ct];
        if(valor[ct]<=menor) menor=valor[ct];
    }
    
    this.txtMaior.setText(String.valueOf(maior));
    this.txtMenor.setText(String.valueOf(menor));
    }//GEN-LAST:event_jButton1ActionPerformed

